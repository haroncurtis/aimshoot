package haron.aimshoot.vo;

public class ViewVO {
	private String menu_view;
	private String join_view;
	private String profile_view;
	private String view_name;
	private String view_path;
	private String menu;
	private String menu_form;
	
	public String getMenu_view() {
		return menu_view;
	}
	public void setMenu_view(String menu_view) {
		this.menu_view = menu_view;
	}
	public String getJoin_view() {
		return join_view;
	}
	public void setJoin_view(String join_view) {
		this.join_view = join_view;
	}
	public String getProfile_view() {
		return profile_view;
	}
	public void setProfile_view(String profile_view) {
		this.profile_view = profile_view;
	}
	public String getView_name() {
		return view_name;
	}
	public void setView_name(String view_name) {
		this.view_name = view_name;
	}
	public String getView_path() {
		return view_path;
	}
	public void setView_path(String view_path) {
		this.view_path = view_path;
	}
	public String getMenu() {
		return menu;
	}
	public void setMenu(String menu) {
		this.menu = menu;
	}
	public String getMenu_form() {
		return menu_form;
	}
	public void setMenu_form(String menu_form) {
		this.menu_form = menu_form;
	}
}
