package haron.aimshoot.vo;

public class LangVO {

	private int no;
	private String lang_type;
	private String lang_display;
	private String lang_type_view;

	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getLang_type() {
		return lang_type;
	}
	public void setLang_type(String lang_type) {
		this.lang_type = lang_type;
	}
	public String getLang_display() {
		return lang_display;
	}
	public void setLang_display(String lang_display) {
		this.lang_display = lang_display;
	}
	public String getLang_type_view() {
		return lang_type_view;
	}
	public void setLang_type_view(String lang_type_view) {
		this.lang_type_view = lang_type_view;
	}
}
